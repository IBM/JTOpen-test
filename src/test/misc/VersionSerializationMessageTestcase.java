  public static boolean genericTest(StringBuffer sb, String[][] methodTests,
      byte[] serializedObject) throws Exception {
    boolean passed = true;
    ByteArrayInputStream bais = new ByteArrayInputStream(serializedObject);
    ObjectInputStream ois = new ObjectInputStream(bais);
    AS400Message message = (AS400Message) ois.readObject();
    sb.append("message=" + message + "\n");
    for (int i = 0; i < methodTests.length; i++) {
      String methodName = methodTests[i][0];
      String expectedAnswer = methodTests[i][1];
      Object answer = JDReflectionUtil.callMethod_O(message, methodName);
      String stringAnswer;
      if (answer instanceof GregorianCalendar) {
        stringAnswer = "" + ((GregorianCalendar) answer).getTimeInMillis();
      } else if (answer instanceof byte[]) {
        stringAnswer = bytesToString((byte[]) answer);
      } else {
        stringAnswer = "" + answer;
      }
      if (!expectedAnswer.equals(stringAnswer)) {
        sb.append("For method " + methodName + " got '" + stringAnswer
            + "' sb '" + expectedAnswer + "'\n");
        passed = false;
      }
    }
    return passed;

  }

